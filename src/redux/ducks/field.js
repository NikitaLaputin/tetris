export default (state = defaultState, action) => {
  const { type, payload } = action;

  switch (type) {
    case MERGE:
      const { shape, position } = payload;

      return mergeMatrix(state, shape, position);

    case DESTROY_ROW:
      return payload;

    case RESET:
      return defaultState;

    default:
      return state;
  }
};

export const mergeField = payload => ({
  type: MERGE,
  payload
});

export const destroyRow = payload => ({
  type: DESTROY_ROW,
  payload
});
